#!/usr/bin/env node
// Inline the six expertise assets into a generated TS module so @aiengineercv/core works in BOTH
// Node and the browser with no runtime filesystem reads. packages/core/assets remains the single
// source of truth; run `pnpm gen:core` (or this script) after editing any asset.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const assetsDir = join(root, 'packages', 'core', 'assets');
const outDir = join(root, 'packages', 'core', 'src', 'generated');

// Resolve `yaml` from the core package, where it's a dependency.
const require = createRequire(join(root, 'packages', 'core', 'package.json'));
const YAML = require('yaml');

const read = (name) => readFileSync(join(assetsDir, name), 'utf8');
const lit = (v) => JSON.stringify(v, null, 2);

const profileSchema = JSON.parse(read('profile.schema.json'));
const taxonomy = YAML.parse(read('taxonomy.yaml'));
const rubric = YAML.parse(read('rubric.yaml'));
const questionBank = YAML.parse(read('questions.yaml'));
const generationSpec = read('generation-spec.md');
const guardrailDoc = read('guardrail.md');

const out = `// AUTO-GENERATED by scripts/gen-core-assets.mjs - do not edit.
// Edit the originals in packages/core/assets, then run \`pnpm gen:core\`.
import type { Taxonomy, Rubric, QuestionBank } from '../assetTypes.js';

export const profileSchema: Record<string, unknown> = ${lit(profileSchema)};

export const taxonomy: Taxonomy = ${lit(taxonomy)};

export const rubric: Rubric = ${lit(rubric)};

export const questionBank: QuestionBank = ${lit(questionBank)};

export const generationSpec: string = ${JSON.stringify(generationSpec)};

export const guardrailDoc: string = ${JSON.stringify(guardrailDoc)};
`;

mkdirSync(outDir, { recursive: true });
writeFileSync(join(outDir, 'assets.ts'), out, 'utf8');
console.log(`Generated packages/core/src/generated/assets.ts (${(out.length / 1024).toFixed(1)} KB)`);
